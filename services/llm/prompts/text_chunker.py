"""
文本 Chunker 的 LLM Prompt 模板。

供 services.indexing.chunkers.text_chunker.TextChunker 使用。
通过 .format() 注入：text_sample（两个模板）、failed_regex（仅第二次模板）。

设计要点（2026-07-16 修订）：
- 要求 LLM 先原样抄写样本中真实出现的标题行，再归纳 regex——
  防止 LLM 凭先验直接输出 '^第X章' 类锚定而忽略行首前缀/缩进
  （实测事故：标题为 '变脸之初卷 第一章 …'，LLM 两次给出 ^第 锚定，0 命中）
- 不在提示词中给出示例 regex——旧版示例 '^第[一二三…]章.*$' 被 LLM
  照抄，成为错误锚定的来源
- 输出约定为最后一行 'regex: <表达式>'，代码按此提取
"""

# ── 第一次尝试（首次样本量）───────────────────────────────

FIRST_ATTEMPT_PROMPT = (
    "以下是一本中文书的开头部分节选。请完成两步：\n"
    "1. 在文本中找出真实出现的章节标题行，原样抄写其中 1-2 行"
    "（包括行首的缩进和前缀）；\n"
    "2. 归纳一个正则表达式（将配合 re.MULTILINE 使用），"
    "要求能完整匹配你抄写的标题行。\n\n"
    "注意：标题行可能带有前缀（如卷名）或行首缩进（如全角空格），"
    "使用 ^ 锚定时必须容纳这些前缀。\n"
    "如果文本中找不到任何章节标题行，最后一行输出 regex: NONE。\n\n"
    "最后一行严格按此格式输出（不要 markdown 代码块）：\n"
    "regex: <你的正则表达式>\n\n"
    "文本：\n"
    "{text_sample}"
)

# ── 第二次尝试（扩展样本量，附上一次失败信息）─────────────

SECOND_ATTEMPT_PROMPT = (
    "你此前为这本书提供的章节标题正则表达式是：{failed_regex}\n"
    "它在全文验证中匹配数不足，已被判定失败。最常见的失败原因是 "
    "^ 锚定过死，没有容纳标题行的前缀（如卷名）或行首缩进（如全角空格）。\n\n"
    "以下是更大范围的文本节选。请重新完成两步：\n"
    "1. 找出文本中真实出现的章节标题行，原样抄写其中 1-2 行"
    "（包括行首的缩进和前缀）；\n"
    "2. 归纳能完整匹配这些行的正则表达式（将配合 re.MULTILINE 使用）。\n\n"
    "如果文本中找不到任何章节标题行，最后一行输出 regex: NONE。\n\n"
    "最后一行严格按此格式输出（不要 markdown 代码块）：\n"
    "regex: <你的正则表达式>\n\n"
    "文本：\n"
    "{text_sample}"
)
